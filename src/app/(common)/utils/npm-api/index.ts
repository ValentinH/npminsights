import { addYears, endOfYear, format } from 'date-fns';
import { NpmDailyDownloads, NpmRangeData } from './types';
import { SIX_HOURS_IN_SECONDS } from '../consts';
import http from '../http';

const FIRST_AVAILABLE_DATE = '2015-01-01';

export const getPackageInsights = async (packageName = '', sinceDate = FIRST_AVAILABLE_DATE) => {
  if (process.env.NODE_ENV === 'development') {
    return MOCK_DATA;
  }
  const allDailyDownloads = await getAllDailyDownloads(packageName, sinceDate);

  return {
    total: getSumOfDownloads(allDailyDownloads),
    lastDay: allDailyDownloads[allDailyDownloads.length - 1].downloads,
    lastDayPreviousWeek: allDailyDownloads[allDailyDownloads.length - 8].downloads,
    lastWeek: getSumOfDownloads(allDailyDownloads.slice(-7)),
    previousWeek: getSumOfDownloads(allDailyDownloads.slice(-14, -7)),
    lastMonth: getSumOfDownloads(allDailyDownloads.slice(-30)),
    previousMonth: getSumOfDownloads(allDailyDownloads.slice(-60, -30)),
    lastYear: getSumOfDownloads(allDailyDownloads.slice(-365)),
    previousYear: getSumOfDownloads(allDailyDownloads.slice(-730, -365)),
    allDailyDownloads,
  };
};

const getAllDailyDownloads = async (packageName: string, sinceDate: string) => {
  const ranges = getListOfRangesSinceStart(sinceDate);
  const rangesResponses = await Promise.all(
    ranges.map(({ start, end }) => {
      return http.get<NpmRangeData>(
        `https://api.npmjs.org/downloads/range/${start}:${end}/${packageName}`,
        {
          next: {
            revalidate: SIX_HOURS_IN_SECONDS,
          },
        }
      );
    })
  );
  const allDownloads = rangesResponses.reduce<NpmDailyDownloads[]>((acc, { data }) => {
    return [...acc, ...data.downloads];
  }, []);

  const lastDayWithData = allDownloads.findLastIndex(({ downloads }) => downloads > 0);
  const allDownloadsUntilLastDayWithData = allDownloads.slice(0, lastDayWithData + 1);

  // remove all leading zeros
  const firstNonZeroIndex = allDownloadsUntilLastDayWithData.findIndex((p) => p.downloads !== 0);
  if (firstNonZeroIndex > 0) {
    return allDownloadsUntilLastDayWithData.slice(firstNonZeroIndex);
  }

  return allDownloadsUntilLastDayWithData;
};

const getSumOfDownloads = (downloads: NpmDailyDownloads[]) => {
  return downloads.reduce((acc, { downloads }) => {
    return acc + downloads;
  }, 0);
};

const getListOfRangesSinceStart = (sinceDate: string) => {
  const startDate = new Date(sinceDate);
  const today = new Date();
  const ranges = [];
  let current = startDate;
  while (current <= today) {
    ranges.push({
      start: format(current, 'yyyy-MM-dd'),
      end: format(endOfYear(current), 'yyyy-MM-dd'),
    });
    current = addYears(current, 1);
  }
  return ranges;
};

